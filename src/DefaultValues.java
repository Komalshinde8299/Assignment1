public class DefaultValues {
    int a ;
    String b;
    boolean d;
    long e;
    byte h;
    float y;
    double u;
    char w;

    public static void main(String[] args) {
        DefaultValues obj = new DefaultValues();

       System.out.println("Null values of the primitive data type is");
        System.out.println("int"+obj.a);
        System.out.println("String "+ obj.b);
        System.out.println("boolean "+ obj.d);
        System.out.println("long "+ obj.e);
        System.out.println("byte "+ obj.h);
        System.out.println("float "+ obj.y);
        System.out.println("double "+obj.u);
        System.out.println("char "+ obj.w);    }


}
